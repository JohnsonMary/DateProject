package Practice4;



public class Positions {
	public static void main(String[]args) {
	
	String alphabet="ABCDEFGHIJKLMNOPQRSTUVWMXYZ";
	
	System.out.println(alphabet.length());
	System.out.println(alphabet.indexOf("M"));
	System.out.println(alphabet.charAt(11));
	System.out.println(alphabet.lastIndexOf("M"));
	System.out.println(alphabet.indexOf("M",13));
	System.out.println(alphabet.toLowerCase());
	System.out.println(alphabet.isEmpty());
	System.out.println(alphabet.startsWith("A"));
	System.out.println(alphabet.startsWith("B"));
	System.out.println(alphabet.endsWith("Z"));
	System.out.println(alphabet.contains("EG"));
	
		
		
		
		
		
		
		
		
	}
	
}
