package Repl.it;

public class LameCalculator {
	
	public int plus(int num1,int num2) {
		
		return num1+num2;
		
	}
	public int minus(int n1,int n2) {
		
		return n1-n2;
	}
	public int multiply(int num1,int num2) {
		
		return num1*num2;
		
	}
	public int divide(int n1,int n2) {
		
		return n1/n2;
		
	}


}
