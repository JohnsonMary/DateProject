package Pratice7;

public class whilel {
	public static void main(String[] args) {
		
        int k=1;
		
		do {
			System.out.print("&");
			k++;
		}while(k<=5);
		
		
		
		
		for(int i=0; i<5; i++) {
			System.out.println("Hello World!");
		}
		
		for(int n=10;n>=1;n--) {
			System.out.print(n+",");
			
		}
		
		for(int m=1; m<=10; m++) {
			System.out.print(m+",");
		}
		
	}

}
