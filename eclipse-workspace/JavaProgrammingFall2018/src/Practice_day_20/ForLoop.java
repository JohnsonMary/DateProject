package Practice_day_20;

public class ForLoop {
	public static void main(String[] args) {
		
		for(int i=1;i<=5; i++) {
			System.out.println("Happy birthday!!!- "+i);
		}
		
		for(int j=5; j<=100; j+=50) {
			System.out.println("HelloWorld -"+j);
			
		}
		for(int k=10; k>=1;k--) {
			System.out.println("Hello-"+k);
			
		}
		
		
	}

}
