package Practice_22_NestedLoops;

public class LoopAssignment2 {
	public static void main(String[] args) {
		
		for(int h=1; h<=3; h++) {
			for(int k=10;k>=h; k--) {
				for(int n=1; n<=k; n++) {
					
					System.out.print(n);
					
				}System.out.println();
				
				
			}
			
			
			
			
		}
		
		
		
		
	
	
	
		
	}

}
