package Practice_22_NestedLoops;

public class LoopAssignment{
	public static void main(String[] args) {
		
		for(int i=1; i<=10; i++) {
			for(int n=1; n<=i; n++) {
				
				System.out.print(n);
				
			}System.out.println();
			
		}
	     
		for(int h=10; h>=1; h--) {
			for(int l=1;l<=h;l++) {
				
				System.out.print(l);
				
		}System.out.println();
			
		}
		
		for (int g=1; g<=10; g++) {
			for(int d=1; d<=g; d++) {
				
				System.out.print(d);
				
			}System.out.println();
			
			
		}
		for(int a=10;a>=1; a--) {
			for(int c=1; c<=a; c++) {
				
				System.out.print(c);
				
			}System.out.println();
			
		}
		
		
		
	}

}
