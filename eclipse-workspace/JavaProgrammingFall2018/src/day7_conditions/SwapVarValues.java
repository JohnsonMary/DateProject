package day7_conditions;

public class SwapVarValues {
	public static void main(String[] args) {
		int apples=100;
		int oranges=200;
		  
	    int tempContainer=apples;
	    apples=oranges;
	    oranges=tempContainer;
	    
	    System.out.println("Oranges: "+oranges+" apples: "+apples);
	    
	    
	    
		 
		
		
		
		
		
		
	}

}
