package day21_Loops04;

public class Continue {
	public static void main(String[] args) {
		
	
	
		for(int num=1; num<=50; num++) {
			
			if(num%5==0) {
			 continue;
				
			 
			}else if(num%2==0) {
				break;
				
			}System.out.println(num +" ");
		} 
		
			

			
			
		}
		
		
		
		
		
		
		
		
	}


