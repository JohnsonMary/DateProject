package Practic_day41;

public class Book {

	private String title,author;
	private int pages;
	
	
	
	public String getTitle() {
		
		return title;
		
		
	}
	public void setTitle(String titleNew) {
		
		title=titleNew;
		
	}
	public String getAuthor() {
		
		return author;
	}
	public void setAuthor(String newAuthor) {
		
		author=newAuthor;
	}
	
	public int getPages() {
		
		return pages;
	}
	public void setPages(int page) {
		
		pages=page;
	}
}
