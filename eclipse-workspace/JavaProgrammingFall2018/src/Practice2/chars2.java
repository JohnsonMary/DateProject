		char letter='a';
	    letter++;
	    System.out.println(letter);
	    
	    int num=letter;
	    System.out.println(num);
	    
	    char hello='d';
	    System.out.println(hello);
	    
	    int num1=hello;
	    System.out.println(num1);
	    
